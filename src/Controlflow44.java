
public class Controlflow44 {

	public static void main(String[] args) {
		// switch
		switch (5) {
		case (1):
			System.out.println("더 커야 합니다.");
			break;
		case (2):
			System.out.println("더 커야 합니다.");
			break;
		case (3):
			System.out.println("더 커야 합니다.");
			break;
		case (4):
			System.out.println("더 커야 합니다.");
			break;
		case (5):
			System.out.println("5입니다.");
			break;
		}
}
}