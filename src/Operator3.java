
public class Operator3 {

	public static void main(String[] args) { // country
		// +연산자 문자열을 연결해주는 연산자
		
		System.out.println('1'+1); // '': char도 정수
		System.out.println((char)('1'+1)); 
		System.out.println("1"+1); // "": 문자열
		
		System.out.println("1"+1+2+3); // 모든 숫자의 문자화
		System.out.println("1"+(1+2+3)); // 16
		System.out.println(1+2+3+"1"); // 끝 숫자를 통한 문자화
		
		
	}
	{
		int i, j, k;
		// 대입 우선순위
		
		i = j = k = 5; // 우측부터 좌로 한 칸씩
		
	}

}
